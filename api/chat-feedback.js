// Vercel Serverless Function — /api/chat-feedback.js
// Stores chat feedback (no-op on Vercel — logs only)

export default async function handler(req, res) {
  res.setHeader('Access-Control-Allow-Origin', '*');
  res.setHeader('Access-Control-Allow-Methods', 'POST, OPTIONS');
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type');

  if (req.method === 'OPTIONS') {
    return res.status(204).end();
  }

  if (req.method !== 'POST') {
    return res.status(405).json({ error: 'Method not allowed' });
  }

  let payload;
  try {
    payload = typeof req.body === 'string' ? JSON.parse(req.body) : req.body;
  } catch {
    return res.status(400).json({ error: 'Invalid JSON' });
  }

  // Log feedback (extend this to store in a DB if needed)
  console.log('[Career Pakistan Feedback]', JSON.stringify(payload));

  return res.status(200).json({ ok: true });
}
